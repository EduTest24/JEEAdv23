function calculateMarks(question) {
  // Calculate marks based on the type of question and the user's attempt
  let marks = 0;
  if (question.attempted) {
    if (question.type === "SCQ") {
      if (question.userSelectedOption === question.correctAnswer) {
        marks = 3;
      } else if (question.userSelectedOption === null) {
        marks = 0;
      } else {
        marks = -1;
      }
    } else if (question.type === "MCQ") {
      const selectedOptions = question.userSelectedOption;
      const correctOptions = question.correctAnswer;
      const allOptions = question.options.length;

      let correctCount = 0;
      let isIncorrect = false;

      selectedOptions.forEach((optionIndex) => {
        if (correctOptions.includes(optionIndex)) {
          correctCount++;
        } else {
          isIncorrect = true;
        }
      });

      if (isIncorrect) {
        marks = -2;
      } else if (correctCount === correctOptions.length) {
        marks = 4;
      } else if (correctCount === 3 && allOptions === 4) {
        marks = 3;
      } else if (correctCount === 2 && correctOptions.length >= 3) {
        marks = 2;
      } else if (correctCount === 1 && correctOptions.length >= 2) {
        marks = 1;
      } else {
        marks = 0;
      }
    } else if (question.type === "Numerical") {
      const correctAnswer = question.correctAnswer;
      const userAnswer = question.userSelectedOption;

      if (Number.isInteger(correctAnswer)) {
        if (userAnswer === correctAnswer) {
          marks = 4;
        } else {
          marks = 0;
        }
      } else {
        if (Math.abs(userAnswer - correctAnswer) < 0.01) {
          marks = 3;
        } else {
          marks = 0;
        }
      }
    }
  }

  return marks;
}

