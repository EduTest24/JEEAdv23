// Function to update the sidebar with question indices
function updateSidebar2() {
  const correctQuestionsElement = document.getElementById("correct-questions2");
  const incorrectQuestionsElement = document.getElementById(
    "incorrect-questions2"
  );
  const notAttemptedQuestionsElement = document.getElementById(
    "not-attempted-questions2"
  );
  const markedQuestionsElement = document.getElementById("marked-questions2");
  const reviewedQuestionsElement = document.getElementById(
    "reviewed-questions2"
  );

  correctQuestionsElement.innerHTML = "";
  incorrectQuestionsElement.innerHTML = "";
  notAttemptedQuestionsElement.innerHTML = "";
  markedQuestionsElement.innerHTML = "";
  reviewedQuestionsElement.innerHTML = "";

  questions.forEach((question, index) => {
    let questionBox = `<div class="question-box2" onclick="jumpToQuestion2(${index})">${
      index + 1
    }</div>`;
    let isCorrect = false;

    if (question.attempted) {
      if (question.type === "SCQ") {
        if (question.userSelectedOption === question.correctAnswer) {
          isCorrect = true;
        }
      } else if (question.type === "MCQ") {
        const selectedOptions = question.userSelectedOption;
        const correctOptions = question.correctAnswer;

        const allSelectedOptionsCorrect = selectedOptions.every((opt) =>
          correctOptions.includes(opt)
        );
        const allSelectedOptionsAreInCorrectAnswer = selectedOptions.every(
          (opt) => correctOptions.includes(opt)
        );

        if (
          allSelectedOptionsCorrect &&
          selectedOptions.length === correctOptions.length
        ) {
          isCorrect = true;
        } else if (
          allSelectedOptionsCorrect &&
          selectedOptions.length < correctOptions.length
        ) {
          isCorrect = true;
        } else {
          isCorrect = false;
        }
      } else if (question.type === "Numerical") {
        const correctAnswer = question.correctAnswer;
        const userAnswer = question.userSelectedOption;

        if (Number.isInteger(correctAnswer)) {
          if (userAnswer === correctAnswer) {
            isCorrect = true;
          }
        } else {
          if (Math.abs(userAnswer - correctAnswer) < 0.01) {
            isCorrect = true;
          }
        }
      }

      if (isCorrect) {
        correctQuestionsElement.innerHTML += questionBox;
      } else {
        incorrectQuestionsElement.innerHTML += questionBox;
      }
    } else {
      notAttemptedQuestionsElement.innerHTML += questionBox;
    }

    if (question.isMarked) {
      markedQuestionsElement.innerHTML += questionBox;
    }

    if (question.isReviewed) {
      reviewedQuestionsElement.innerHTML += questionBox;
    }
  });
}

